# Also we can do slicing in list

Friends = ["Prakhar","Navneet","Aryan","Durgesh","Jivan","Dushyant","Aman","Natansh"]
print(Friends[0:])

# Output --------->       ['Prakhar', 'Navneet', 'Aryan', 'Durgesh', 'Jivan', 'Dushyant', 'Aman', 'Natansh']

print(Friends[0])   # Output --------->       Prakhar  
print(Friends[0:4]) # Output --------->       ['Prakhar', 'Navneet', 'Aryan', 'Durgesh']
print(Friends[0:7]) # Output --------->       ['Prakhar', 'Navneet', 'Aryan', 'Durgesh', 'Jivan', 'Dushyant', 'Aman']